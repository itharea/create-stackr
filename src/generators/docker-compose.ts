/**
 * Uppercase service name with dashes converted to underscores, used as the
 * env-var prefix: `scout` → `SCOUT`, `my-service` → `MY_SERVICE`.
 */
function envPrefix(serviceName: string): string {
  return serviceName.toUpperCase().replace(/-/g, '_');
}

/**
 * Indent every line of a rendered block by two spaces so it sits inside
 * the `services:` top-level map. Preserves trailing newlines.
 */
function indentEntry(block: string): string {
  return block
    .split('\n')
    .map((line) => (line.length > 0 ? '  ' + line : line))
    .join('\n');
}

