interface InfraPorts {
  db: number; // published DB port on host (Postgres)
  redis: number; // published Redis port on host
}

/**
 * Assign external host ports for each service's Postgres and Redis in
 * declaration order, starting at 5432 / 6379 and incrementing.
 */
function assignInfraPorts(services: readonly ServiceEntry[]): Record<string, InfraPorts> {
  const out: Record<string, InfraPorts> = {};
  let dbPort = 5432;
  let redisPort = 6379;
  for (const svc of services) {
    out[svc.name] = { db: dbPort++, redis: redisPort++ };
  }
  return out;
}

function renderServiceDbBlock(
  svc: ServiceEntry,
  ports: InfraPorts,
  projectName: string,
  _mode: 'dev' | 'prod'
): string {
  const upper = envPrefix(svc.name);
  const defaultDbName = `${projectName}_${svc.name}`.replace(/-/g, '_');

  return indentEntry(
    `${svc.name}_db:\n` +
      `  image: postgres:16-alpine\n` +
      `  container_name: ${projectName}-${svc.name}_db\n` +
      `  restart: unless-stopped\n` +
      `  environment:\n` +
      `    POSTGRES_USER: \${${upper}_DB_USER:-postgres}\n` +
      `    POSTGRES_PASSWORD: \${${upper}_DB_PASSWORD}\n` +
      `    POSTGRES_DB: \${${upper}_DB_NAME:-${defaultDbName}}\n` +
      `  ports:\n` +
      `    - "${ports.db}:5432"\n` +
      `  volumes:\n` +
      `    - ${svc.name}_postgres_data:/var/lib/postgresql/data\n` +
      `  healthcheck:\n` +
      `    test: ["CMD-SHELL", "pg_isready -U \${${upper}_DB_USER:-postgres} -d \${${upper}_DB_NAME:-${defaultDbName}}"]\n` +
      `    interval: 10s\n` +
      `    timeout: 5s\n` +
      `    retries: 3\n`
  );
}

function renderServiceRedisBlock(
  svc: ServiceEntry,
  ports: InfraPorts,
  projectName: string,
  _mode: 'dev' | 'prod'
): string {
  const upper = envPrefix(svc.name);
  return indentEntry(
    `${svc.name}_redis:\n` +
      `  image: redis:7-alpine\n` +
      `  container_name: ${projectName}-${svc.name}_redis\n` +
      `  restart: unless-stopped\n` +
      `  command: redis-server --appendonly yes --requirepass \${${upper}_REDIS_PASSWORD:-redis-password}\n` +
      `  ports:\n` +
      `    - "${ports.redis}:6379"\n` +
      `  volumes:\n` +
      `    - ${svc.name}_redis_data:/data\n` +
      `  healthcheck:\n` +
      `    test: ["CMD", "redis-cli", "--raw", "incr", "ping"]\n` +
      `    interval: 10s\n` +
      `    timeout: 3s\n` +
      `    retries: 3\n`
  );
}

function renderServiceRestApiBlock(
  svc: ServiceEntry,
  _ports: InfraPorts,
  projectName: string,
  _mode: 'dev' | 'prod',
  authService: ServiceEntry | null
): string {
  const upper = envPrefix(svc.name);
  const defaultDbName = `${projectName}_${svc.name}`.replace(/-/g, '_');
  const authServiceUrl = authService
    ? `http://${authService.name}_rest_api:${authService.backend.port}`
    : null;

  // All environment and depends_on lines are at 4-space indent *before*
  // indentEntry adds its own 2-space prefix — final indent is 6 spaces
  // which sits one level deeper than the parent `environment:` / `depends_on:` keys.
  const envLines: string[] = [
    `    NODE_ENV: development`,
    `    DATABASE_URL: postgresql://\${${upper}_DB_USER:-postgres}:\${${upper}_DB_PASSWORD}@${svc.name}_db:5432/\${${upper}_DB_NAME:-${defaultDbName}}`,
    `    API_HOST: 0.0.0.0`,
    `    API_PORT: ${svc.backend.port}`,
    `    LOG_LEVEL: debug`,
    `    REDIS_HOST: ${svc.name}_redis`,
    `    REDIS_PORT: 6379`,
    `    REDIS_PASSWORD: \${${upper}_REDIS_PASSWORD:-redis-password}`,
  ];

  if (authServiceUrl && svc.kind !== 'auth') {
    envLines.push(`    AUTH_SERVICE_URL: ${authServiceUrl}`);
  }

  const dependsLines: string[] = [
    `    ${svc.name}_db:`,
    `      condition: service_healthy`,
    `    ${svc.name}_redis:`,
    `      condition: service_healthy`,
  ];

  if (authServiceUrl && svc.kind !== 'auth') {
    dependsLines.push(`    ${authService!.name}_rest_api:`);
    dependsLines.push(`      condition: service_started`);
  }

  return indentEntry(
    `${svc.name}_rest_api:\n` +
      `  build:\n` +
      `    context: ./${svc.name}/backend\n` +
      `    target: rest-api-dev\n` +
      `    dockerfile: Dockerfile\n` +
      `  container_name: ${projectName}-${svc.name}_rest_api\n` +
      `  restart: unless-stopped\n` +
      `  environment:\n` +
      envLines.join('\n') +
      '\n' +
      `  ports:\n` +
      `    - "${svc.backend.port}:${svc.backend.port}"\n` +
      `  volumes:\n` +
      `    - ./${svc.name}/backend:/app:cached\n` +
      `    - /app/node_modules\n` +
      `  depends_on:\n` +
      dependsLines.join('\n') +
      '\n' +
      `  env_file:\n` +
      `    - ./${svc.name}/backend/.env\n`
  );
}

