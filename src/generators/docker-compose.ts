/**
 * Programmatic docker-compose.yml renderer. See
 * `plans/phase2_multi_service_generation.md` §D.3 for fidelity
 * requirements and the reference shape in
 * `/Users/ghost/Documents/FRC 9483/team9483-stack/docker-compose.yml`.
 *
 * Strategy: we serialize individual service/volume entries via
 * `yaml.stringify` (so docker-compose fields stay quoted/escaped
 * correctly) then assemble the top-level file by hand so the marker
 * comments land at the right indent. The `services:` / `volumes:` top-
 * level keys live OUTSIDE the marker block; only the managed entries are
 * inside — this is what lets phase 3's preservation contract work.
 *
 * Output is byte-deterministic for identical inputs (sorted nowhere —
 * iteration order is `config.services[]`).
 */
export function renderDockerCompose(
  config: StackrConfigFile,
  mode: 'dev' | 'prod'
): string {
  if (mode === 'dev') {
    return renderDevCompose(config);
  }
  return renderProdCompose(config);
}

// ---------------------------------------------------------------------------
// Dev compose
// ---------------------------------------------------------------------------

function renderDevCompose(config: StackrConfigFile): string {
  const { services: cfgServices, projectName } = config;

  // Compute per-service external ports so DB/Redis don't collide.
  const portMap = assignInfraPorts(cfgServices);
  const authService = cfgServices.find((s) => s.kind === 'auth') ?? null;

  // Build services map in deterministic order, flattening each service's
  // 2-4 compose entries (db, redis?, rest_api, event_queue?).
  const serviceBlocks: string[] = [];
  const volumeBlocks: string[] = [];

  for (const svc of cfgServices) {
    const ports = portMap[svc.name];

    serviceBlocks.push(
      renderServiceDbBlock(svc, ports, projectName, 'dev')
    );

    const needsRedis = svc.backend.eventQueue || svc.backend.imageUploads || true;
    if (needsRedis) {
      serviceBlocks.push(
        renderServiceRedisBlock(svc, ports, projectName, 'dev')
      );
    }

    serviceBlocks.push(
      renderServiceRestApiBlock(svc, ports, projectName, 'dev', authService)
    );

    if (svc.backend.eventQueue) {
      serviceBlocks.push(
        renderServiceEventQueueBlock(svc, projectName, 'dev')
      );
    }

    // Volume entries
    volumeBlocks.push(`  ${svc.name}_postgres_data:\n    driver: local\n`);
    if (needsRedis) {
      volumeBlocks.push(`  ${svc.name}_redis_data:\n    driver: local\n`);
    }
  }

  const serviceEntries = serviceBlocks.join('\n');
  const volumeEntries = volumeBlocks.join('');

  const header =
    `# All infrastructure for local development\n` +
    `# Usage: docker compose up -d\n` +
    `# Managed by stackr — marker blocks below are regenerated by\n` +
    `# \`stackr add service\`. Edit OUTSIDE the markers to preserve work.\n`;

  return initComposeWithMarkedBlocks(serviceEntries, volumeEntries, { header });
}

// ---------------------------------------------------------------------------
// Prod compose (overlay)
// ---------------------------------------------------------------------------

function renderProdCompose(config: StackrConfigFile): string {
  const { services: cfgServices } = config;
  const portMap = assignInfraPorts(cfgServices);

  const serviceBlocks: string[] = [];

  for (const svc of cfgServices) {
    const ports = portMap[svc.name];

    // db
    serviceBlocks.push(
      indentEntry(
        `${svc.name}_db:\n` +
          `  restart: always\n` +
          `  ports:\n` +
          `    - "127.0.0.1:${ports.db}:5432"\n` +
          `  healthcheck:\n` +
          `    interval: 30s\n` +
          `    timeout: 5s\n` +
          `    retries: 5\n`
      )
    );

    // redis
    serviceBlocks.push(
      indentEntry(
        `${svc.name}_redis:\n` +
          `  restart: always\n` +
          `  ports:\n` +
          `    - "127.0.0.1:${ports.redis}:6379"\n` +
          `  healthcheck:\n` +
          `    interval: 30s\n` +
          `    timeout: 5s\n` +
          `    retries: 5\n` +
          `  deploy:\n` +
          `    resources:\n` +
          `      limits:\n` +
          `        memory: 512M\n` +
          `      reservations:\n` +
          `        memory: 256M\n`
      )
    );

    // rest api
    serviceBlocks.push(
      indentEntry(
        `${svc.name}_rest_api:\n` +
          `  build:\n` +
          `    context: ./${svc.name}/backend\n` +
          `    target: rest-api-prod\n` +
          `    dockerfile: Dockerfile\n` +
          `  restart: always\n` +
          `  environment:\n` +
          `    NODE_ENV: production\n` +
          `    LOG_LEVEL: \${LOG_LEVEL:-info}\n` +
          `  ports:\n` +
          `    - "127.0.0.1:${svc.backend.port}:${svc.backend.port}"\n` +
          `  volumes: []\n` +
          `  healthcheck:\n` +
          `    test: ["CMD", "bun", "-e", "const res = await fetch('http://localhost:${svc.backend.port}/'); process.exit(res.ok ? 0 : 1)"]\n` +
          `    interval: 30s\n` +
          `    timeout: 10s\n` +
          `    retries: 3\n` +
          `    start_period: 30s\n`
      )
    );

    if (svc.backend.eventQueue) {
      serviceBlocks.push(
        indentEntry(
          `${svc.name}_event_queue:\n` +
            `  build:\n` +
            `    context: ./${svc.name}/backend\n` +
            `    target: event-queue-prod\n` +
            `    dockerfile: Dockerfile\n` +
            `  restart: always\n` +
            `  environment:\n` +
            `    NODE_ENV: production\n` +
            `    LOG_LEVEL: \${LOG_LEVEL:-info}\n` +
            `  volumes: []\n`
        )
      );
    }
  }

  const serviceEntries = serviceBlocks.join('\n');
  // Prod overlay has no managed volumes section (volumes come from dev).
  const header =
    `# Production overlay for docker-compose.yml\n` +
    `# Usage: docker compose -f docker-compose.yml -f docker-compose.prod.yml up -d\n` +
    `# Managed by stackr — marker blocks below are regenerated by\n` +
    `# \`stackr add service\`. Edit OUTSIDE the markers to preserve work.\n`;

  return initComposeWithMarkedBlocks(serviceEntries, '', { header });
}

